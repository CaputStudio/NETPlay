    private void scanForWinner(int[][] matriz) {
        boolean win = true;
        boolean lose = true;
        boolean draw = false;
        for (int i = 0; i < 3; i++) {
            for (int j = 0; j < 3; j++) {
                win = win && (matriz[i][j] == id);
                lose = lose && (matriz[i][j] == foe);

                draw = draw || (matriz[i][j] == V);
            }

            if (win) {
                showWinner(true);
                return;
            } else {
                win = true;
            }
            if (lose) {
                showWinner(false);
                return;
            } else {
                lose = true;
            }

            for (int j = 0; j < 3; j++) {
                win = win && (matriz[j][i] == id);
                lose = lose && (matriz[j][i] == foe);
            }
            if (win) {
                showWinner(true);
                return;
            } else {
                win = true;
            }
            if (lose) {
                showWinner(false);
                return;
            } else {
                lose = true;
            }
        }

        if (!draw) {
            showDraw();
        }

        for (int i = 0; i < 3; i++) {
            win = win && (matriz[i][i] == id);
            lose = lose && (matriz[i][i] == foe);
        }
        if (win) {
            showWinner(true);
            return;
        } else {
            win = true;
        }
        if (lose) {
            showWinner(false);
            return;
        } else {
            lose = true;
        }

        for (int i = 0, j = 2; i < 3; i++, j--) {
            win = win && (matriz[i][j] == id);
            lose = lose && (matriz[i][j] == foe);
        }
        if (win) {
            showWinner(true);
            return;
        } else {
            win = true;
        }
        if (lose) {
            showWinner(false);
            return;
        } else {
            lose = true;
        }
    }

    private void showWinner(boolean win) {
        String msg = win ? "Venceu!" : "Perdeu";
        JOptionPane.showMessageDialog(this, "Você " + msg);
        this.dispose();
    }

    private void showDraw() {
        JOptionPane.showMessageDialog(this, "Deu velha!");
        this.dispose();
    }
