    public void setJogada(int x, int y) {
        if (turn == id) {
            managerInput.sendMove(x, y, id, foe);
            fields[x][y] = (int) id;
            setButtons(fields);
            turn = foe;
        } else {
            JOptionPane.showMessageDialog(this, "Não é sua vez de jogar.");
        }
    }

    public void postJogada(int x, int y) {
        fields[x][y] = (int) foe;
        setButtons(fields);
    }

